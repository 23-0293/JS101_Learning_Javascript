let day = "mon";

switch (day) {
  case ("sun"):
    console.log("sunday");
    break;
  case ("mon"):
    console.log("monday");
    break;
  case ("tue"):
    console.log("tuesday");
    break;
  case ("thr"):
    console.log("thrusday");
    break;
  case ("fri"):
    console.log("friday");
    break;
  case ("sat"):
    console.log("saturday");
    break;
  default:
    console.log("worng input");
}